        private static void Renorm(int[] t)
        {
            int total = 0;
            for (int s = 0; s < 16; s++)
            {
                t[s] = Math.Max(1, t[s] >> 1);
                total += t[s];
            }
            t[16] = total;
        }

        private static int[] BuildCumulativeInto(int[] freq, int[] dest)
        {
            dest[0] = 0;
            for (int s = 0; s < 16; s++) dest[s + 1] = dest[s] + freq[s];
            return dest;
        }
